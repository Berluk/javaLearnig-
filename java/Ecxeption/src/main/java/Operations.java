public class Operations {

    static double add(double x, double y) {
        return x + y;
    }

    static double sub(double x, double y) {
        return x - y;
    }

    static double mult(double x, double y) {
        return x * y;
    }

    static double div(double x, double y) {
        return x / y;
    }
}
